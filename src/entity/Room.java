package entity;

public class Room {
    int id;
    RoomType roomType;
    int price;
    int hotelId;

    public Room(int id, RoomType roomType, int price, int hotelId) {
        this.id = id;
        this.roomType = roomType;
        this.price = price;
        this.hotelId = hotelId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public RoomType getRoomType() {
        return roomType;
    }

    public void setRoomType(RoomType roomType) {
        this.roomType = roomType;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getHotelId() {
        return hotelId;
    }

    public void setHotelId(int hotelId) {
        this.hotelId = hotelId;
    }
}
